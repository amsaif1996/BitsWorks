
package datatype;


public class DataType {

   
    public static void main(String[] args)
    {
        boolean b=true;
        char c='a';
        short s= 326;
        int i=12312312;
        float f=2.3f;
        double d=2125.325;
        
        
        
        System.out.printf("boolean b= %b \n",b); //'%' eta die print korte chaile printf use korte hoi
        System.out.printf("Character c= %c \n",c);
        System.out.println("s="+s);
        System.out.println("i="+i); //%d use korte hobe int+short er jonno
        System.out.printf("f= %.2f \n",f); //.2f disi karon tahole '.' er por 2 ghor nibe
        System.out.println("d="+d);
        
        
    }
    
}
